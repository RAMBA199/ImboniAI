export function t(key: string, language: Language = 'en', replacements?: Record<string, string>): string {
  const value = translations[language]?.[key] || translations.en[key] || key;
  if (!replacements) return value;
  return Object.entries(replacements).reduce((result, [key, replacement]) => {
    return result.replace(new RegExp(`\{${key}\}`, 'g'), replacement);
  }, value);
}

export function tArray(key: string, language: Language = 'en'): string[] {
  const value = translations[language]?.[key];
  return Array.isArray(value) ? value : [];
}
